// Day Finder with Switch
// User inputs a number (1–7) → print weekday.


let day=4;
switch(day){
    case 1:
        console.log("monday");
        break;
    case 2:
        console.log("tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;    
    default :
       console.log("Invalid Day");
        
}